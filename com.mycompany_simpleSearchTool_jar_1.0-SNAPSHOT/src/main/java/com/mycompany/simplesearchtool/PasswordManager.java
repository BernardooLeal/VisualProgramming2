    public void loadAccounts(){
        try {
            String filePath = System.getProperty("user.home") + "/passwords_" + loggedInUser + ".txt"; // I used chat to understand what to do since without the user.home the file was in a unwritable directory
            File userFile = new File(filePath);
            if (userFile.exists()) {
                BufferedReader br = new BufferedReader(new FileReader(userFile));
                List<String> accounts = new ArrayList<>();
                String line;
                //line = br.readLine();
                while ((line = br.readLine()) != null) {
                    accounts.add(line);
                }
                br.close();
                jlstInfo.setListData(accounts.toArray(new String[0])); // also gpt here to be able to convert accounts into elements for the list
                
            }
        } catch (IOException e) {
            e.printStackTrace();
            System.out.println("problema no load");
        }
    }
    
    private void saveAccount(String accountName, String password) { //Users/bernardoleal/NetBeansProjects/PasswordManager/passwords_user1.txt
        try {
            String filePath = System.getProperty("user.home") + "/passwords_" + loggedInUser + ".txt";
            File userFile = new File(filePath);
            BufferedWriter bw = new BufferedWriter(new FileWriter(userFile, true));
            bw.write("Username: " + accountName + " | " + "Password:" + password);
            bw.newLine();
            bw.close();
        } catch (IOException e) {
            e.printStackTrace();
            System.out.println("problema no save");
        }
    }

