    private void jBtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAddActionPerformed
        // TODO add your handling code here:
        String username = jTxtUser.getText();
        String password = jtxtPass.getText();
        
        if (!username.isEmpty() && !password.isEmpty()) {
            saveAccount(username, password);
            jTxtUser.setText("");
            jtxtPass.setText("");
            loadAccounts();
        } else {
            JOptionPane.showMessageDialog(null, "Enter both username and password", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        String userInfo = "Username: " + username + " | Password: " + password;
        DefaultListModel<String> listModel = new DefaultListModel<>();
        for (int i = 0; i < jlstInfo.getModel().getSize(); i++) {
            listModel.addElement(jlstInfo.getModel().getElementAt(i)); // add the info of the list into the new default list
        }
         listModel.addElement(userInfo);
    
        jlstInfo.setModel(listModel); 

   
    }//GEN-LAST:event_jBtnAddActionPerformed

